    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        String medicineName = listMedicines.getSelectedValue();
        MedicineList allMedicinesList = new MedicineList();
        String strAmount = txtAmount1.getText();
        boolean valid = true;
        int amount = 0;
        try{
            amount = Integer.parseInt(strAmount);
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(new JFrame(), "Please select a valid amount(in integer)","Required input",JOptionPane.ERROR_MESSAGE);
            valid = false;
        }
        if (medicineName == null) {
            JOptionPane.showMessageDialog(new JFrame(), "Please select a medicine from the All medicines list","Required input",JOptionPane.ERROR_MESSAGE);
        }
        try {
            allMedicinesList = MedicinesController.getAllMedicines();
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(SecretaryGiveMedicine.class.getName()).log(Level.SEVERE, null, ex);
        }
        ArrayList<Medicine> requestMedicines = secretary.getRequestMedicines();
        ArrayList<Medicine> allMedicines = allMedicinesList.getAllMedicines();
        boolean isRequest = false;
        
        if (allMedicines != null && valid == true) {
            for (Medicine medicine : allMedicines) {
                if (medicine.getName().equals(medicineName)) {
                    int stock = medicine.getQuantity();
                    medicine.setQuantity(stock + amount);
                    if (requestMedicines!= null) {
                        for (Medicine reqMedicine : requestMedicines) {
                            if (reqMedicine.getName().equals(medicine.getName())) {
                                MedicinesController doctorRequestMedicine = new DoctorRequestMedicineCreate();
                                try {
                                    doctorRequestMedicine.addQuantity(secretary,medicine);
                                    isRequest = true;
                                } catch (IOException | ClassNotFoundException e) {
                                    e.printStackTrace();
                                }
                            }
                        } 
                    }                   
                    if (isRequest == false) {
                        MedicinesController secretaryMedicine = new SecretaryMedicineCreate();
                        try {
                            secretaryMedicine.addQuantity(secretary,medicine);
                        } catch (IOException | ClassNotFoundException e) {
                            e.printStackTrace();
                        }  
                    }
                }
            }
            fillList();
            JOptionPane.showMessageDialog(null, "Successfully added to stock", "Success", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnApproveActionPerformed

