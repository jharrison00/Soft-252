    private void fillList(){
        DefaultListModel listModelMedicine = new DefaultListModel();
        DefaultListModel listModelStock = new DefaultListModel();
        DefaultListModel listModelRequest = new DefaultListModel();
        MedicineList allMedicinesList = null;
        try {
            allMedicinesList = MedicinesController.getAllMedicines();
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(SecretaryGiveMedicine.class.getName()).log(Level.SEVERE, null, ex);
        }
        ArrayList<Medicine> allMedicines = allMedicinesList.getAllMedicines();
        if (allMedicines != null) {
            for (Medicine medicine : allMedicines) {
                listModelMedicine.addElement(medicine.getName());
                listModelStock.addElement(medicine.getQuantity());
            }
        }
        
        ArrayList<Medicine> requestMedicines = secretary.getRequestMedicines();
        if (requestMedicines != null) {
            for (Medicine medicine : requestMedicines){
                listModelRequest.addElement(medicine.getName());
            }
        }
        
        listMedicines.setModel(listModelMedicine);
        listAmount.setModel(listModelStock);
        listRequests.setModel(listModelRequest);
    }
    
       
    
